


# 假设一个房间有四盏灯，初始时全是关着的，即状态为0
a = 0b0000

while 1:
    # 每次输入一个数，操作一盏灯，就是开变成关，关变成开
    opt,num = map(int,input().split())
    # 9表示结束
    if opt == 9:
        break
    if opt == 0:
        a = a ^ (1 << (num - 1))

    # 查找当前位
    #0b1011
    # 1 表示 0001 << (1 - 1) = 0001
    #0b1011
    #0b0001
    if opt == 1:
        print(a & (1 << (num - 1)) != 0)

